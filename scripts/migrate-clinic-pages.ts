const extractTextContent = (html: string, startMarker: string, endMarker?: string): string => {
  const startIndex = html.indexOf(startMarker);
  if (startIndex === -1) return '';

  const content = html.substring(startIndex + startMarker.length);
  if (endMarker) {
    const endIndex = content.indexOf(endMarker);
    return endIndex === -1 ? content : content.substring(0, endIndex);
  }
  return content;
};

